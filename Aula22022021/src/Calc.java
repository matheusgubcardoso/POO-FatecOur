public class Calc {
    // atributos
    
    void som(double v1, double v2) {
        
        double res = v1 + v2;
        System.out.println("Soma = " + res + "\n"); 
        
    }

    void sub(double v1, double v2) {
        
        double res = v1 - v2;
        System.out.println("Subtração = " + res + "\n"); 
        
    }

    void mul(double v1, double v2) {
        
        double res = v1 * v2;
        System.out.println("Multiplicação = " + res + "\n"); 
        
    }

    void div(double v1, double v2) {
        
        double res = v1 / v2;
        System.out.println("Divisão = " + res + "\n"); 
        
    }
}
